% --- Executes on button press in pushbutton7.
function pushbutton7_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton7 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
    try
        secret = imread(handles.pathsecretimage1);
    catch exception
        set(handles.text7,'String','Please Enter the image');
        set(handles.text7,'Visible','On');
    end
    
    try
        path = handles.pathtosave1;
    catch exception
        set(handles.text7,'String','Please Enter path to save');
        set(handles.text7,'Visible','On');
    end
    
    try
        imgname = get(handles.edit1,'String');
        if isempty(imgname)
            set(handles.text7,'String','Please Enter image name .jpg');
            set(handles.text7,'Visible','On');
        end
    catch exception
        set(handles.text7,'String','Please Enter image name .jpg');
        set(handles.text7,'Visible','On');
    end
        
    outputimage = Extractwatermark(secret);
    path1 = strcat(path,'\',imgname);
    imwrite(outputimage,path1);
    
        
    set(handles.text7,'String','Done');
    set(handles.text7,'ForegroundColor','green');
    set(handles.text7,'Visible','On');
    figure; imshow(outputimage);


