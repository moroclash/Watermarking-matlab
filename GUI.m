% --- Executes on button press in pushbutton21.
function pushbutton21_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton21 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
    try
        cover = imread(handles.pathcoverimage);
        secret = imread(handles.pathsecretimage);
    catch exception
        set(handles.text23,'String','Please Enter All images');
        set(handles.text23,'Visible','On');
    end
    
    try
        path = handles.pathtosave;
    catch exception
        set(handles.text23,'String','Please Enter path to save');
        set(handles.text23,'Visible','On');
    end
    
    try
        imgname = get(handles.edit3,'String');
        if isempty(imgname)
            set(handles.text23,'String','Please Enter image name .jpg');
            set(handles.text23,'Visible','On');
        end
    catch exception
        set(handles.text23,'String','Please Enter image name .jpg');
        set(handles.text23,'Visible','On');
    end
        
    outputimage = watermark(cover,secret);
    outputimage = OPAP(cover,outputimage,3);
    path1 = strcat(path,'\',imgname);
    imwrite(outputimage,path1);
        
    set(handles.text23,'String','Done');
    set(handles.text23,'ForegroundColor','green');
    set(handles.text23,'Visible','On');
    figure; imshow(outputimage);
        
    
    
    

